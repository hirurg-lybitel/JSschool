/**
  * Описание задачи: Напишите функцию, которая разворачивает массив в обратном порядке.
  * Ожидаемый результат: [1, 2, 3] => [3, 2, 1]
  * @param {Array} array - Массив любых элементов
  * @returns {Array}
*/
function returnArr() {
  let count = [1, 2, 3];
  const reversed = count.reverse();
  return reversed;
}
